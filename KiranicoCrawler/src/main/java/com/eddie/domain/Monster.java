package com.eddie.domain;

public class Monster {
    private String  mName;
    private String  affMaterials;

    public Monster() {
    }

    public String getmName() {
        return mName;
    }

    public void setmName(String mName) {
        this.mName = mName;
    }

    public String getAffMaterials() {
        return affMaterials;
    }

    public void setAffMaterials(String affMaterials) {
        this.affMaterials = affMaterials;
    }


}
